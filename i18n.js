const LANG_STORAGE_KEY = 'oshi-desk-lang';
const SUPPORTED_LANGS = ['ko', 'en', 'ja', 'zh-TW'];
const LANG_PATH_MAP = {
  ko: 'ko',
  en: 'en',
  ja: 'ja',
  'zh-TW': 'zh-tw',
};

function detectLangFromPath() {
  const firstSegment = window.location.pathname.split('/').filter(Boolean)[0]?.toLowerCase();
  if (!firstSegment) return null;
  if (firstSegment === 'zh-tw' || firstSegment === 'zh-hant') return 'zh-TW';
  return SUPPORTED_LANGS.find((lang) => LANG_PATH_MAP[lang] === firstSegment) || null;
}

function resolveLang(code) {
  if (!code) return 'ko';
  const normalized = code.trim();
  if (SUPPORTED_LANGS.includes(normalized)) return normalized;
  const lower = normalized.toLowerCase();
  if (lower.startsWith('zh')) {
    if (lower.includes('tw') || lower.includes('hk') || lower.includes('hant')) return 'zh-TW';
    return 'zh-TW';
  }
  const base = lower.split('-')[0];
  if (base === 'en') return 'en';
  if (base === 'ja') return 'ja';
  if (base === 'ko') return 'ko';
  return 'ko';
}

function detectLang() {
  const pathLang = detectLangFromPath();
  if (pathLang) return pathLang;

  try {
    const stored = localStorage.getItem(LANG_STORAGE_KEY);
    if (stored) return resolveLang(stored);
  } catch (_) {}
  const browser = navigator.languages?.[0] || navigator.language || 'ko';
  return resolveLang(browser);
}

function getNested(obj, path) {
  return path.split('.').reduce((acc, key) => (acc && acc[key] != null ? acc[key] : null), obj);
}

function translate(lang, key, vars) {
  const value = getNested(I18N[lang], key) ?? getNested(I18N.ko, key) ?? key;
  if (!vars) return value;
  return String(value).replace(/\{(\w+)\}/g, (_, name) => (vars[name] != null ? vars[name] : `{${name}}`));
}

let currentLang = 'ko';

function getLang() {
  return currentLang;
}

function applyLanguage(lang) {
  currentLang = resolveLang(lang);
  const dict = I18N[currentLang];

  document.documentElement.lang = currentLang === 'zh-TW' ? 'zh-Hant' : currentLang;

  const page = document.body.dataset.i18nPage;
  const pageMeta = page ? getNested(dict, `${page}.meta`) : null;
  const meta = pageMeta || dict.meta;

  const desc = document.querySelector('meta[name="description"]');
  if (desc) desc.setAttribute('content', meta.description);
  document.title = meta.title;
  const ogTitle = document.querySelector('meta[property="og:title"]');
  if (ogTitle) ogTitle.setAttribute('content', meta.title);
  const ogDesc = document.querySelector('meta[property="og:description"]');
  if (ogDesc) ogDesc.setAttribute('content', meta.description);
  const ogLocale = document.querySelector('meta[property="og:locale"]');
  const localeMap = { ko: 'ko_KR', en: 'en_US', ja: 'ja_JP', 'zh-TW': 'zh_TW' };
  if (ogLocale) ogLocale.setAttribute('content', pageMeta ? (localeMap[currentLang] || 'ko_KR') : dict.meta.locale);
  const twitterTitle = document.querySelector('meta[name="twitter:title"]');
  if (twitterTitle) twitterTitle.setAttribute('content', meta.title);
  const twitterDesc = document.querySelector('meta[name="twitter:description"]');
  if (twitterDesc) twitterDesc.setAttribute('content', meta.description);
  const canonical = document.querySelector('link[rel="canonical"]');
  const ogUrl = document.querySelector('meta[property="og:url"]');
  if (ogUrl && canonical?.href) ogUrl.setAttribute('content', canonical.href);

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.dataset.i18n;
    const useHtml = el.dataset.i18nHtml === 'true';
    const text = translate(currentLang, key);
    if (useHtml) el.innerHTML = text;
    else el.textContent = text;
  });

  document.querySelectorAll('[data-i18n-attr]').forEach((el) => {
    el.dataset.i18nAttr.split(';').forEach((pair) => {
      const [attr, key] = pair.split(':').map((s) => s.trim());
      if (attr && key) el.setAttribute(attr, translate(currentLang, key));
    });
  });

  document.querySelectorAll('[data-i18n-list]').forEach((el) => {
    const items = getNested(dict, el.dataset.i18nList);
    if (Array.isArray(items)) {
      el.innerHTML = items.map((item) => `<li>${item}</li>`).join('');
    }
  });

  document.querySelectorAll('[data-i18n-cells]').forEach((row) => {
    const items = getNested(dict, row.dataset.i18nCells);
    if (Array.isArray(items)) {
      row.querySelectorAll('td').forEach((cell, index) => {
        if (items[index] != null) cell.textContent = items[index];
      });
    }
  });

  document.querySelectorAll('.lang-option').forEach((btn) => {
    const active = btn.dataset.lang === currentLang;
    btn.classList.toggle('active', active);
    btn.setAttribute('aria-pressed', String(active));
    btn.setAttribute('aria-selected', String(active));
  });

  const langBadge = document.querySelector('.lang-current');
  if (langBadge) {
    const badges = { ko: 'KO', en: 'EN', ja: 'JA', 'zh-TW': 'ZH' };
    langBadge.textContent = badges[currentLang] || 'KO';
  }

  const langToggle = document.getElementById('langToggle');
  if (langToggle) {
    langToggle.setAttribute('aria-label', dict.lang.label);
    langToggle.setAttribute('title', dict.lang.label);
  }

  try {
    localStorage.setItem(LANG_STORAGE_KEY, currentLang);
  } catch (_) {}

  window.dispatchEvent(new CustomEvent('languagechange', { detail: { lang: currentLang } }));
}

function initI18n() {
  currentLang = detectLang();
  applyLanguage(currentLang);

  document.querySelectorAll('.lang-option').forEach((btn) => {
    btn.addEventListener('click', () => {
      applyLanguage(btn.dataset.lang);
      const menu = document.getElementById('langMenu');
      const toggle = document.getElementById('langToggle');
      if (menu) menu.hidden = true;
      if (toggle) toggle.setAttribute('aria-expanded', 'false');
    });
  });

  const langToggle = document.getElementById('langToggle');
  const langMenu = document.getElementById('langMenu');
  if (langToggle && langMenu) {
    langToggle.addEventListener('click', (e) => {
      e.stopPropagation();
      const open = langMenu.hidden;
      langMenu.hidden = !open;
      langToggle.setAttribute('aria-expanded', String(open));
    });

    document.addEventListener('click', () => {
      langMenu.hidden = true;
      langToggle.setAttribute('aria-expanded', 'false');
    });

    langMenu.addEventListener('click', (e) => e.stopPropagation());
  }
}

window.OshiI18n = { t: translate, getLang, applyLanguage, I18N };
